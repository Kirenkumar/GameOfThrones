function getRandom () {
   return Math.floor((Math.random() * 0) + 1);
}
var num = getRandom();
var current_class;
$(window).load(function() {
    /* Act on the event */
    switch(num) {
        case 1: current_class = 'stark';
                break;
        case 2: current_class = 'baratheon';
                break;
        case 3: current_class = 'lannister'
                break;
        default: current_class = 'stark';
    }

    $('.login-container').addClass('login-'+current_class);


});

